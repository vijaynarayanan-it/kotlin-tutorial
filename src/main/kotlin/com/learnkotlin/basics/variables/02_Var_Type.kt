package com.learnkotlin.basics.variables

/**
 * var means variable.
 *
 * we can modify and reassign the var variable data.
 * the data type for var variable is inferred by the compiler.
 *
 */
fun main() {
    // assigning 10
    var id = 10

    // reassigning id variable
    id = 30

    println("Id is : $id")
}